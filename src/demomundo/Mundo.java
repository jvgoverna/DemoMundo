    public void encontrarVizinhosDeUmaPessoa(Pessoa p){
        int posX = p.getX();
        int posY = p.getY();
     
        for(int i = posX-1; i <= posX + 1; i++){
            for(int j = posY-1; j <= posY + 1; j++){
                if(mapa[i][j] == 5 && (i != posX || j != posY)){
                    encontrarPessoasPorCoordenada(i, j);
                }
            }
        }  
}

